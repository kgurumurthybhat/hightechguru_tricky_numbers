# Python Solution for Q0005 Maximum Gift

import time


def max_gift(arr):
    n = len(arr)
    m = len(arr[0])
    for i in range(n - 1, -1, -1):
        for j in range(m - 1, -1, -1):
            if i == n - 1 and j == m - 1:
                gift = arr[i][j]
            else:
                if i < n - 1 and j < m - 1:
                    gift = max(arr[i + 1][j], arr[i][j + 1])
                    arr[i][j] += gift
                elif j >= m - 1:
                    gift = arr[i + 1][j]
                    arr[i][j] += gift
                else:  # i >= n - 1:
                    gift = arr[i][j + 1]
                    arr[i][j] += gift
    return arr[0][0]


